import { cleanTimestamp } from "./utils"

describe("cleanTimestamp", () => {
  // @prettier-ignore
  const cases = [
    ["00:00", "00:00"],
    ["00:00.0", "00:00"],
    ["00:00.00", "00:00"],
    ["00:00.000", "00:00"],
    ["00:00.05", "00:00.05"],
    ["00:00.050", "00:00.05"],
    ["00:00.5", "00:00.5"],
    ["00:00.50", "00:00.5"],
    ["00:00.500", "00:00.5"],
    ["00:00.005", "00:00.005"],
    ["00:00.055", "00:00.055"],
    ["00:00.555", "00:00.555"],
    ["00:00:00", "00:00"],
    ["00:00:00.0", "00:00"],
    ["00:00:00.00", "00:00"],
    ["00:00:00.000", "00:00"],
    ["12:34", "12:34"],
    ["12:34.0", "12:34"],
    ["12:34.00", "12:34"],
    ["12:34.000", "12:34"],
    ["00:12:34.000", "12:34"],
    ["12:34.05", "12:34.05"],
    ["12:34.050", "12:34.05"],
    ["12:34.5", "12:34.5"],
    ["00:12:34.5", "12:34.5"],
    ["12:34.50", "12:34.5"],
    ["12:34.500", "12:34.5"],
    ["12:34.005", "12:34.005"],
    ["12:34.055", "12:34.055"],
    ["12:34.555", "12:34.555"],
    ["00:12:34.555", "12:34.555"],
    ["12:34:56.555", "12:34:56.555"],
  ]

  test.each(cases)("%p -> %p", (from, to) =>
    expect(cleanTimestamp(from)).toEqual(to),
  )
})
